package org.openhds.report.beans;

public class IdentificationBookBean {
	
	String lowestLevelValue;
	String lowestLevelName;
	String locExtId;
	String indivExtId;
	String name;
	String dob;
	String gender;
	
	public String getLowestLevelValue() {
		return lowestLevelValue;
	}
	
	public void setLowestLevelValue(String lowestLevelValue) {
		this.lowestLevelValue = lowestLevelValue;
	}
	
	public String getLowestLevelName() {
		return lowestLevelName;
	}
	
	public void setLowestLevelName(String lowestLevelName) {
		this.lowestLevelName = lowestLevelName;
	}
	
	public String getLocExtId() {
		return locExtId;
	}
	
	public void setLocExtId(String locExtId) {
		this.locExtId = locExtId;
	}
	
	public String getIndivExtId() {
		return indivExtId;
	}

	public void setIndivExtId(String indivExtId) {
		this.indivExtId = indivExtId;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public String getDob() {
		return dob;
	}
	
	public void setDob(String dob) {
		this.dob = dob;
	}
	public String getGender() {
		return gender;
	}
	
	public void setGender(String gender) {
		this.gender = gender;
	}
}
